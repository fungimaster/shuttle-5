          <h3>Pitch</h3>
          <p>A frequent complaint we get on the quality of sessions at Ã˜redev is "did not reflect description." It is, therefore, crucial that you take the time to write a good abstract, and, perhaps even more importantly, a great title.</p>
          <p>Remember that your goal is <strong>not</strong> to make your presentation sound better than it is. A good description, as opposed to the infamous sales pitch, truthfully reflects the contents and sets the expections at the exact right level. This substantially increases the chances of presenting for the right audience.</p>

          <h4>Title</h4>
          <p>If your session touches specific technologies or frameworks, consider including them in your title. Carefully ponder your choice of words as there won't be many of them, making each one important.</p>
          <p>Have a soft max of 50 characters, and a hard one of 70 characters. "Your 2019 CSS Layout Toolkit" is 28 characters and "Beyond Conway's Law: Meet the Social Side of Your Code" is 54 characters. Both give you enough information to decide whether you should dig in deeper or look elsewhere.</p>

